#include <stdio.h>
#include "../../include/scanm.h"

// Essa função vai ser usada na diagonalização de matrizes
int scanm(int tamanho, double a[tamanho][tamanho])
{
    for (int i = 0; i < 0; i++)
    {
        for (int j = 0; j < 0; j++)
        {
            scanf("%lf", &a[i][j]);
        }
    }
}